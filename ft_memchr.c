void	*ft_memchr(const void *s, int c, unsigned int n)
{
	int		i;
	char	*s2;

	i = 0;
	s2 = s;
	while (n--)
	{
		if (s2[i] == c)
			return (s + i);
		i++;
	}
	return (0);
}
