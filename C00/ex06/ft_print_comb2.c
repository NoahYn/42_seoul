#include<unistd.h>

void	ft_print_comb2(void)
{
	int		i0;
	int		i1;
	char	comb[5];

	comb[2] = ' ';
	i0 = 0;
	i1 = 1;
	while (i0 < 98)
	{
		comb[0] = i0 / 10 + '0';
		comb[1] = i0 % 10 + '0';
		while (i1 < 100)
		{
			comb[3] = i1 / 10 + '0';
			comb[4] = i1 % 10 + '0';
			i1++;
			write(1, comb, 5);
			write(1, ", ", 2);
		}
		i1 = ++i0 + 1;
	}
	write(1, "98 99", 5);
	return ;
}
