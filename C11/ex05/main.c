#include "ft_do_op.h"

int	main(int argc, char **argv)
{
	if (argc != 4)
		return (0);
	if ((argv[2][0] == '+' || argv[2][0] == '-'
		|| argv[2][0] == '/' || argv[2][0] == '*' || argv[2][0] == '%')
		&& argv[2][1] == 0)
		ft_do_op(argv);
	else
		write(1, "0\n", 2);
	return (0);
}
